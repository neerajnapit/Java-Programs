package OOPS.Inheritance.Example;

public class Box {
    double length, width, height;
    Box() {
        this.length = -1;
        this.width = -1;
        this.height = -1;
    }
    Box(double l, double w, double h) {
        this.length = l;
        this.width = w;
        this.height = h;
    }
}
